// Get current language
export function getCurrentLanguage() {
  try {
    const saved = localStorage.getItem(LANGUAGE_KEY);
    if (saved && LANGUAGES[saved]) {
      return saved;
    }
  } catch {
    // Ignore errors
  }

  // Auto-detect from browser
  const browserLang = navigator.language || navigator.languages?.[0] || 'en';
  const langCode = browserLang.split('-')[0].toLowerCase();

  // Check if we support this language
  if (LANGUAGES[langCode]) {
    return langCode;
  }

  // Default to English
  return 'en';
}

// Set current language
export function setCurrentLanguage(langCode) {
  try {
    if (LANGUAGES[langCode]) {
      localStorage.setItem(LANGUAGE_KEY, langCode);
      // Dispatch event for cross-tab sync
      window.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: langCode } }));
      return true;
    } else {
      console.error(
        'ğŸŒ [i18n] Invalid language code:',
        langCode,
        'Available languages:',
        Object.keys(LANGUAGES)
      );
    }
  } catch (error) {
    console.error('ğŸŒ [i18n] Error setting language:', error);
  }
  return false;
}

// Get translation
export function t(key, fallback = null) {
  const lang = getCurrentLanguage();
  const translations = TRANSLATIONS[lang] || TRANSLATIONS.en;
  const result = translations[key] || fallback || key;
  return result;
}

// Get language info
export function getLanguageInfo(langCode) {
  return LANGUAGES[langCode] || LANGUAGES.en;
}

// Get all supported languages
export function getSupportedLanguages() {
  return Object.values(LANGUAGES);
}
